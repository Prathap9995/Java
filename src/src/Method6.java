
public class Method6 {
	public static void main(String[] args) {
		System.out.println(20+10);     //operator overloading
		System.out.println("p"+"s");
		System.out.println(20+10+"p"+"s"); //+ operator only
		System.out.println(20+10+"p"+"s"+100+200); //String + integer = concatenation so output is..
	int a = 10;
	int b = 20;
	int c =30;
	System.out.println(a);  //Multiple line declaration is not recomended in project level
	System.out.println(c);
	System.out.println(a);
	System.out.println(a+ " "+b+ " " +c); // Hence we declared all the variable in single line
	
}}

